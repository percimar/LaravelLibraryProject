        $timeSlotsInAWeek = 60; //12 one-hour slots a day, 5 times a week
        $rooms = Room::all()->each(function ($room) {
            //type + last digit of location, i.e. Study Room at 10.2.2 becomes "Study Room 2"
            $room->name = $room->type . ' ' . explode('.', $room->location)[2];
            $room->availability = round(100 - (($room->bookings->count() / 60) * 100), 2);
        });
        return view('studyRooms', ['rooms' => $rooms]);
    }

